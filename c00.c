#include <unistd.h>

void	aski_print(int a, int b)
{
	char	c;
	char	d;
	char	n;
	char	m;

	c = (a / 10) + '0';
	d = (a % 10) + '0';
	n = (b / 10) + '0';
	m = (b % 10) + '0';
	write(1, &c, 1);
	write(1, &d, 1);
	write(1, " ", 1);
	write(1, &n, 1);
	write(1, &m, 1);
	if (a + b != 98 + 99)
	{
		write(1, ",", 1);
		write(1, " ", 1);
	}
}

void	ft_print_comb2(void)
{
	int	a ;
	int	b ;

	a = 0;
	while (a <= 99)
	{
		b = a + 1;
		while (b <= 99)
		{
			aski_print(a, b);
			b++;
		}
		a++;
	}
}
