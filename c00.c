#include <unistd.h>

void	print_depth(int depth, int i, int n, int *num1)
{
	char	arr;

	if (depth == n)
	{
		i = 0;
		while (i < n)
		{
			arr = num1[i] + '0';
			write(1, &arr, 1);
			i++;
		}
		if (10 - num1[0] != n)
		{
			write(1, ",", 1);
			write(1, " ", 1);
		}
		return ;
	}
	if (i > 9)
		return ;
	num1[depth] = i;
	print_depth(depth + 1, i + 1, n, num1);
	num1[depth] = 0;
	print_depth(depth, i + 1, n, num1);
}

void	ft_print_combn(int n)
{
	int	num1[10];

	print_depth(0, 0, n, num1);
}