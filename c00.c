#include <unistd.h>

void	print_number(int b, char *num)
{
	int	a;

	a = 0;
	while (a <= b)
	{
		num[a] = num[a] + '0';
		write(1, &num[a], 1);
		a++;
	}
}

int	number_count(int c)
{
	int	count;

	count = 0;
	while (c > 0)
	{
		while (c != 0)
		{
			c = c / 10;
			count++;
		}
	}
	return (count);
}

void	ft_putnbr(int nb)
{
	int		i;
	int		n;
	char	num[12];

	if (nb == -2147483648)
	{
		write (1, "-2147483648", 11);
		return ;
	}
	if (nb == 0)
		write(1, "0", 1);
	if (nb < 0)
	{
		nb = nb * -1;
		write(1, "-", 1);
	}
	i = number_count(nb) - 1;
	n = number_count(nb) - 1;
	while (i >= 0)
	{
		num[i] = nb % 10;
		nb = nb / 10;
		i--;
	}
	print_number (n, num);
}
